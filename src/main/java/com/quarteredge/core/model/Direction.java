package com.quarteredge.core.model;

/**
 * Represents the direction of a trade.
 *
 * <p>This enum defines the possible directions for a trade, including:
 *
 * <ul>
 *   <li>BUY: Indicates a buy order.
 *   <li>SELL: Indicates a sell order.
 *   <li>PENDING: Indicates a pending order.
 * </ul>
 *
 * @author King Simmons
 * @version 1.0
 * @since 1.0
 */
public enum Direction {
    /** Indicates a buy order. */
    BUY,

    /** Indicates a sell order. */
    SELL,
    /** Indicates a pending order. */
    PENDING
}
